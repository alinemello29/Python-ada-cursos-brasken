# > Operações matemáticas (aritméticas)

""" 
- soma: +
- subtração: -
- multiplicação: *
- divisão: /
- divisão inteiros: //
- resto da divisão: %
- potência: **
"""

numero1 = 10 
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3)
print(2 ** 3)

